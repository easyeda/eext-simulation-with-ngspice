import fs from "node:fs";
import path from "node:path";

const rootDir = path.resolve(import.meta.dirname, "..");
const sourceDir = path.join(rootDir, "wasm-build", "work", "ngspice-46", "src", "xspice", "icm");
const outputPath = path.join(rootDir, "wasm-lib", "ngspice-xspice-codemodels.js");
const codeModels = ["spice2poly", "analog", "digital", "xtradev", "xtraevt", "table", "tlines"];
const chunkSize = 48 * 1024;

const encoded = {};
for (const name of codeModels) {
	const filePath = path.join(sourceDir, name, `${name}.cm`);
	const base64 = fs.readFileSync(filePath).toString("base64");
	encoded[`${name}.cm`] = base64.match(new RegExp(`.{1,${chunkSize}}`, "g")) ?? [];
}

const content = `/* Generated by wasm-build/embed-xspice-codemodels.mjs. */\n` +
	`(function(root){\n` +
	`  root.__JLC_NGSPICE_XSPICE_CODEMODELS = ${JSON.stringify(encoded)};\n` +
	`})(typeof globalThis !== "undefined" ? globalThis : window);\n`;

fs.writeFileSync(outputPath, content);
console.log(`Wrote ${outputPath}`);
